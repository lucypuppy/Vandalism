package de.nekosarekawaii.vandalism.injection.mixins.fix.minecraft;

import net.minecraft.client.gui.screen.multiplayer.MultiplayerServerListWidget;
import net.minecraft.client.option.ServerList;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

import java.util.List;
import java.util.concurrent.CopyOnWriteArrayList;

@Mixin(MultiplayerServerListWidget.class)
public abstract class MixinMultiplayerServerListWidget {

    @Shadow
    public List<MultiplayerServerListWidget.ServerEntry> servers;

    @Inject(method = "setServers", at = @At("HEAD"))
    private void fixCCME(ServerList servers, CallbackInfo ci) {
        this.servers = new CopyOnWriteArrayList<>();
    }

}
