package de.nekosarekawaii.vandalism.injection.mixins.fix.minecraft;

import net.minecraft.client.render.BufferBuilder;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(BufferBuilder.class)
public abstract class MixinBufferBuilder {

    @Shadow private int elementOffset;

    @Inject(method = "grow(I)V", at = @At("HEAD"))
    private void forceThrowGrowLimitExceeded(final int size, final CallbackInfo ci) {
        if (this.elementOffset > this.elementOffset + size) {
            throw new IllegalStateException("BufferBuilder grow limit exceeded (size: " + size + ")");
        }
    }

}
