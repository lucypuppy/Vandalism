package de.nekosarekawaii.vandalism.injection.mixins.integration;

import de.nekosarekawaii.vandalism.injection.access.ISession;
import net.minecraft.client.session.Session;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Unique;

@Mixin(Session.class)
public abstract class MixinSession implements ISession {

    @Unique
    private boolean vandalism$inflicted = false;

    @Override
    public boolean vandalism$isSelfInflicted() {
        return this.vandalism$inflicted;
    }

    @Override
    public void vandalism$markSelfInfliction() {
        this.vandalism$inflicted = true;
    }

}
